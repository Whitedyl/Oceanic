    private void addBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBTNActionPerformed
        // TODO add your handling code here:
        //creating variable to parse after validation
        int age;
        //taking user inputs and storing in strings
        String name = nameTF.getText();
        String fishermanID = idTF.getText();
        String ageString = ageTF.getText();
        String fishingArea = fishingAreaTF.getText();
        String fishingLevel = fishingLevelTF.getText();
        
        //Ensure inputs are filled out and not null
        if (name.isEmpty() || fishermanID.isEmpty() || ageString.isEmpty() || fishingArea.isEmpty() || fishingLevel.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No field can be empty", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ///creating a try catch and parsing age to an int 
        ///validating the parsed input insuring it's a number using NumberFormatException
        try {
             age = Integer.parseInt(ageString);
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Age must be a number", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Fisherman tempF = new Fisherman(name, fishermanID, fishingArea, fishingLevel, age);
        fishermanList.add(tempF);
        //declare file class, fileOutputStream, ObjectOutputStream
        File f;
        FileOutputStream fStream;
        ObjectOutputStream oStream;
        try {
            f = new File("fishermanList.dat");
            fStream = new FileOutputStream(f);
            oStream = new ObjectOutputStream(fStream);
            //write arraylist to file 
            oStream.writeObject(fishermanList);
            ///close file
            oStream.close();
           

        } catch(IOException e) {
            System.out.println("I/O e: " + e);
        }
//        for(Fisherman temp:fishermanList) {
//            ///System.out.println(temp.printDetails());
//        }
        fishermanTA.setText("Fisherman Added Successfully");
        
    }//GEN-LAST:event_addBTNActionPerformed

