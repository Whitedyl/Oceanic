    private void searchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTNActionPerformed
        // TODO add your handling code here:
        
        // Search for fishermanID
        String fishermanID = JOptionPane.showInputDialog(this, "Enter Fisherman ID to delete:");

            ///ensuring a value was entered and not null
        if (fishermanID == null) {
            JOptionPane.showMessageDialog(this, "Fisherman ID cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // loop created to find the correct fishermanID 
        String fishermanSearch = "";
        boolean found = false;
        for (Fisherman fisherman : fishermanList) {
            if (fisherman.getFishermanID().equals(fishermanID)) { ///calling the getFishID method from fish
                fishermanSearch += fisherman.printDetails();
                found = true; /// set found to true
                break;
            }
        }
        //display found object from fisherman array
        fishermanTA.setText(fishermanSearch);

        // if fisherman id was not found respond to user and ask to try again 
        if (!found) {
            JOptionPane.showMessageDialog(this, "Fish with ID " + fishermanID + " does not exist, please try again", "Fail", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_searchBTNActionPerformed

