    private void startFishingBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startFishingBTNActionPerformed
        // TODO add your handling code here:
        
        String name = nameTF.getText();
        String id = idTF.getText();
        String ageString = ageTF.getText();
        int age = Integer.parseInt(ageString);
        String fishingArea = fishingAreaTF.getText();
        String fishingLevel = fishingLevelTF.getText();
        Fisherman tempF = new Fisherman(name, id, fishingArea, fishingLevel, age);
        fishermanList.add(tempF);
        //declare file class, fileOutputStream, ObjectOutputStream
        File f;
        FileOutputStream fStream;
        ObjectOutputStream oStream;
        try {
            f = new File("fishermanList.dat");
            fStream = new FileOutputStream(f);
            oStream = new ObjectOutputStream(fStream);
            //write arraylist to file 
            oStream.writeObject(fishermanList);
            ///close file
            oStream.close();
            ///textArea.append("Successfully saved file");

        } catch(IOException e) {
            System.out.println("I/O e: " + e);
        }
        for(Fisherman temp:fishermanList) {
            System.out.println(temp.printDetails());
        }
        JOptionPane.showMessageDialog(null, "New Fisherman Added");
        new FishInputGUI().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_startFishingBTNActionPerformed

