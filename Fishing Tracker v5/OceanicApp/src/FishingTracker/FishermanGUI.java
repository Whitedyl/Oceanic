    private void loadFile() {
        File f;
        FileInputStream fStream;
        ObjectInputStream oStream;
        try{
            f = new File("fishermanList.dat");
            fStream = new FileInputStream(f);
            oStream = new ObjectInputStream(fStream);
            ///save the file data to arraylist
            // we need to remind java what type of object we are getting from file
            //cast it to arraylist holding our libraryitem obj
            fishermanList = (ArrayList<Fisherman>)oStream.readObject();
            oStream.close();
        } catch(IOException | ClassNotFoundException e) {
            System.out.println("File input " + e);
        }
    }
